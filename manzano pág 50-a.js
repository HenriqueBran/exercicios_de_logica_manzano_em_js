let numero, quadrado; // Declaração das variáveis numero e quadrado

// Loop de 15 a 200 para calcular os quadrados
for (numero = 15; numero <= 200; numero++) {
    quadrado = numero * numero; // Calculando o quadrado
    console.log("O quadrado de " + numero + " é: " + quadrado); // Exibindo o resultado
}
